package com.newer.sc.common.entity;

public class CcieStu {

	int scid;
	Ccie ccie;
	Student student;
	public int getScid() {
		return scid;
	}
	public void setScid(int scid) {
		this.scid = scid;
	}
	public Ccie getCcie() {
		return ccie;
	}
	public void setCcie(Ccie ccie) {
		this.ccie = ccie;
	}
	public Student getStudent() {
		return student;
	}
	public void setStudent(Student student) {
		this.student = student;
	}
}
